DROP TABLE IF EXISTS Users;
CREATE TABLE Users (
	UserID		INTEGER		PRIMARY KEY ASC AUTOINCREMENT	NOT NULL,
	Username	TEXT		NOT NULL	UNIQUE ON CONFLICT ABORT,
	Password	TEXT		NOT NULL
);

DROP TABLE IF EXISTS Songs;
CREATE TABLE Songs (
	SongID		INTEGER		PRIMARY KEY ASC AUTOINCREMENT	NOT NULL,
	Genre   	TEXT		NOT NULL,
	Title		TEXT		NOT NULL,
	Artist		TEXT		NOT NULL
);

DROP TABLE IF EXISTS Playlists;
CREATE TABLE Playlists (
	PlaylistID	INTEGER		PRIMARY KEY ASC AUTOINCREMENT	NOT NULL,
	UserID		INTEGER		REFERENCES Users(UserID)		NOT NULL,
	Name		TEXT		NOT NULL,
	Private		INTEGER		NOT NULL	DEFAULT(FALSE)
);

DROP TABLE IF EXISTS PlaylistSongs;
CREATE TABLE PlaylistSongs (
	PlaylistID	INTEGER		REFERENCES Playlists(PlaylistID)	ON DELETE CASCADE	NOT NULL,
	SongID		INTEGER		REFERENCES Songs(SongID)	NOT NULL,
	DateAdded	INTEGER		NOT NULL	DEFAULT(0),
	PRIMARY KEY (PlaylistID, SongID)
);

DROP TABLE IF EXISTS UserFavorites;
CREATE TABLE UserFavorites (
	UserID		INTEGER		REFERENCES Users(User)		NOT NULL,
	SongID		INTEGER		REFERENCES Songs(SongID)	NOT NULL,
	DateAdded	INTEGER		NOT NULL	DEFAULT(0),
	PRIMARY KEY (UserID, SongID)
);

